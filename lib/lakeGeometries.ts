// Convert GeoJSON coordinates to SVG path
export function coordinatesToSVGPath(coordinates: number[][][]): string {
  if (!coordinates || !coordinates[0] || coordinates[0].length === 0) {
    return '';
  }

  const ring = coordinates[0];
  
  // Get bounds for normalization
  let minLng = Infinity, maxLng = -Infinity;
  let minLat = Infinity, maxLat = -Infinity;
  
  ring.forEach(([lng, lat]) => {
    minLng = Math.min(minLng, lng);
    maxLng = Math.max(maxLng, lng);
    minLat = Math.min(minLat, lat);
    maxLat = Math.max(maxLat, lat);
  });
  
  // Normalize coordinates to 0-100 range for SVG viewBox
  const normalize = ([lng, lat]: number[]) => {
    const x = ((lng - minLng) / (maxLng - minLng)) * 100;
    const y = ((lat - minLat) / (maxLat - minLat)) * 100;
    return [x, y];
  };
  
  // Create SVG path - use simplified version for performance
  const step = Math.max(1, Math.floor(ring.length / 50)); // Simplify to max 50 points
  let path = '';
  
  for (let i = 0; i < ring.length; i += step) {
    const [x, y] = normalize(ring[i]);
    if (i === 0) {
      path += `M ${x.toFixed(2)},${y.toFixed(2)}`;
    } else {
      path += ` L ${x.toFixed(2)},${y.toFixed(2)}`;
    }
  }
  
  return path + ' Z';
}

// Get lake outline by name
export function getLakeOutline(lakeName: string): string {
  const lakeKey = Object.keys(lakeGeometries).find(key => 
    key.toLowerCase().includes(lakeName.toLowerCase()) ||
    lakeName.toLowerCase().includes(key.toLowerCase())
  );
  
  if (lakeKey && lakeGeometries[lakeKey as keyof typeof lakeGeometries]) {
    return coordinatesToSVGPath(lakeGeometries[lakeKey as keyof typeof lakeGeometries].coordinates);
  }
  
  // Fallback simple shape for lakes not in our data (like Hussain Sagar)
  return 'M 20,30 Q 50,10 80,30 Q 70,70 50,80 Q 30,70 20,30 Z';
}